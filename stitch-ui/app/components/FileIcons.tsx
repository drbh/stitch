// Function to determine file type icon based on mimetype
export const getFileTypeIcon = (type: string) => {
  if (type.startsWith('image/')) {
    return <ImageIcon />;
  } else if (type.startsWith('audio/')) {
    return <AudioIcon />;
  } else if (type.startsWith('video/')) {
    return <VideoIcon />;
  } else {
    return <FileIcon />;
  }
};
